// Function to get a headline based on the date (deterministic but seems random)
export function getHeadlineForDate(date: Date) {
  // Use the day of the month and month to create a deterministic but seemingly random selection
  const day = date.getDate();
  const month = date.getMonth() + 1;
  const seed = (day * month) % newspaperHeadlines.length;
  return newspaperHeadlines[seed];
}
